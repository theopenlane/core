package filecategorygen

import (
	"bytes"
	"fmt"
	"os"
	"sort"
	"text/template"

	"entgo.io/ent/entc"
	"entgo.io/ent/entc/gen"
	"golang.org/x/tools/imports"

	"github.com/theopenlane/entx"
)

// Generator emits the schema-to-file-category lookup used by file persistence.
type Generator struct {
	SchemaPath string
	OutputPath string
	Package    string
}

// New creates a new file category generator.
func New(schemaPath, outputPath string) *Generator {
	return &Generator{
		SchemaPath: schemaPath,
		OutputPath: outputPath,
		Package:    "store",
	}
}

// Hook allows the generator to run in the post-generation hook pipeline.
func (g *Generator) Hook() gen.Hook {
	return func(next gen.Generator) gen.Generator {
		return gen.GenerateFunc(func(graph *gen.Graph) error {
			if err := next.Generate(graph); err != nil {
				return err
			}

			return g.generateFromGraph(graph)
		})
	}
}

const generatedFileTemplate = `// Code generated by filecategorygen. DO NOT EDIT.

package {{.Package}}

// FileCategoryDefaults maps schema names to default file categories.
var FileCategoryDefaults = map[string]string{
{{- range .Entries }}
	"{{ .Schema }}": "{{ .Category }}",
{{- end }}
}
`

type entry struct {
	Schema   string
	Category string
}

// Generate reads schema annotations and writes the generated lookup file.
func (g *Generator) Generate(flags ...string) error {
	graph, err := entc.LoadGraph(g.SchemaPath, &gen.Config{
		BuildFlags: flags,
	})
	if err != nil {
		return fmt.Errorf("loading graph: %w", err)
	}

	return g.generateFromGraph(graph)
}

func (g *Generator) generateFromGraph(graph *gen.Graph) error {
	entries := make([]entry, 0)
	annotation := &entx.FileCategoryAnnotation{}

	for _, schema := range graph.Schemas {
		raw, ok := schema.Annotations[annotation.Name()]
		if !ok {
			continue
		}

		var decoded entx.FileCategoryAnnotation
		if err := decoded.Decode(raw); err != nil || decoded.Category == "" {
			continue
		}

		entries = append(entries, entry{
			Schema:   schema.Name,
			Category: decoded.Category,
		})
	}

	sort.Slice(entries, func(i, j int) bool {
		return entries[i].Schema < entries[j].Schema
	})

	tmpl, err := template.New("generated").Parse(generatedFileTemplate)
	if err != nil {
		return err
	}

	file, err := os.Create(g.OutputPath)
	if err != nil {
		return err
	}
	defer file.Close()

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, struct {
		Package string
		Entries []entry
	}{
		Package: g.Package,
		Entries: entries,
	}); err != nil {
		return err
	}

	formatted, err := imports.Process(g.OutputPath, buf.Bytes(), nil)
	if err != nil {
		return fmt.Errorf("%w: failed to format file", err)
	}

	if _, err := file.Write(formatted); err != nil {
		return err
	}

	return nil
}
