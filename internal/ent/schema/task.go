// Task holds the schema definition for the Task entity
type Task struct {
	SchemaFuncs

	ent.Schema
}

// SchemaTask is the name of the Task schema.
const SchemaTask = "task"

// Name returns the name of the Task schema.
func (Task) Name() string {
	return SchemaTask
}

// GetType returns the type of the Task schema.
func (Task) GetType() any {
	return Task.Type
}

// PluralName returns the plural name of the Task schema.
func (Task) PluralName() string {
	return pluralize.NewClient().Plural(SchemaTask)
}

// Fields of the Task
func (Task) Fields() []ent.Field {
	return []ent.Field{
		field.String("title").
			Comment("the title of the task").
			Annotations(
				entx.FieldSearchable(),
				entgql.OrderField("title"),
			).
			NotEmpty(),
		field.Text("details").
			Comment("the details of the task").
			Optional(),
		field.JSON("details_json", []any{}).
			Optional().
			Annotations(
				entgql.Type("[Any!]"),
			).
			Comment("structured details of the task in JSON format"),
		field.Enum("status").
			GoType(enums.TaskStatus("")).
			Comment("the status of the task").
			Annotations(
				entgql.OrderField("STATUS"),
			).
			Default(enums.TaskStatusOpen.String()),
		field.Time("due").
			GoType(models.DateTime{}).
			Comment("the due date of the task").
			Annotations(
				entgql.OrderField("due"),
			).
			Nillable().
			Optional(),
		field.Time("completed").
			GoType(models.DateTime{}).
			Comment("the completion date of the task").
			Annotations(
				entgql.OrderField("completed"),
			).
			Nillable().
			Optional(),
		field.String("assignee_id").
			Comment("the id of the user who was assigned the task").
			Optional(),
		field.String("assigner_id").
			Optional().
			Comment("the id of the user who assigned the task, can be left empty if created by the system or a service token"),
		field.Bool("system_generated").
			Comment("indicates if the task was generated by the system").
			Annotations(
				directives.ExternalSourceDirectiveAnnotation,
			).
			Default(false),
		field.String("idempotency_key").
			Comment("key to prevent duplicates for auto-generated task based on rules").
			Annotations(
				entgql.Skip(entgql.SkipMutationCreateInput, entgql.SkipMutationUpdateInput),
			).
			Optional(),
		field.Strings("external_reference_url").
			Comment("an optional external reference URL for the task").
			Validate(validator.ValidateURLs()).
			Optional(),
		field.String("parent_task_id").
			Optional().
			Nillable().
			Comment("the parent task this task belongs to"),
	}
}

// Mixin of the Task
func (t Task) Mixin() []ent.Mixin {
	return mixinConfig{
		prefix: "TSK",
		additionalMixins: []ent.Mixin{
			newObjectOwnedMixin[generated.Task](t,
				withParents(InternalPolicy{}, Procedure{}, Control{}, Subcontrol{}, ControlObjective{},
					Program{}, Risk{}, Asset{}, Platform{}, IdentityHolder{}, Scan{}, ActionPlan{}, Task{}),
				withOrganizationOwner(true),
			),
			newCustomEnumMixin(t),
			newCustomEnumMixin(t, withEnumFieldName("environment"), withGlobalEnum()),
			newCustomEnumMixin(t, withEnumFieldName("scope"), withGlobalEnum()),
		},
	}.getMixins(t)
}

