package main

import (
	"encoding/json"
	"flag"
	"fmt"
	"io/fs"
	"os"
	"path/filepath"
	"runtime"
	"sort"
	"strings"

	"github.com/rs/zerolog/log"
	"gopkg.in/yaml.v3"

	"github.com/theopenlane/core/internal/httpserve/server"
)

const (
	jsonFilename      = "openlane.openapi.json"
	yamlFilename      = "openlane.openapi.yaml"
	instancesFilename = "zz_generated_spec_types.go"

	specFilePerm fs.FileMode = 0o600
)

// ErrUnexpectedTypeName is returned when an analyzed type name has no package qualifier
var ErrUnexpectedTypeName = fmt.Errorf("unexpected type name")

func main() {
	emitTypes := flag.Bool("emit-types", false, "regenerate the model instances file from handler analysis instead of the spec")
	flag.Parse()

	run := runSpec
	if *emitTypes {
		run = runEmitTypes
	}

	if err := run(); err != nil {
		log.Fatal().Err(err).Msg("failed to generate OpenAPI specifications")
	}
}

func runSpec() error {
	spec, err := server.GenerateOpenAPISpecDocument()
	if err != nil {
		return fmt.Errorf("build OpenAPI spec: %w", err)
	}

	outputDir, err := specsOutputDir()
	if err != nil {
		return fmt.Errorf("determine specs output directory: %w", err)
	}

	jsonOutputPath := filepath.Join(outputDir, jsonFilename)
	yamlOutputPath := filepath.Join(outputDir, yamlFilename)

	jsonData, err := json.MarshalIndent(spec, "", "  ")
	if err != nil {
		return fmt.Errorf("marshal spec to json: %w", err)
	}

	if err := os.WriteFile(jsonOutputPath, jsonData, specFilePerm); err != nil {
		return fmt.Errorf("write json spec: %w", err)
	}

	var intermediate any
	if err := json.Unmarshal(jsonData, &intermediate); err != nil {
		return fmt.Errorf("prepare data for yaml: %w", err)
	}

	yamlData, err := yaml.Marshal(intermediate)
	if err != nil {
		return fmt.Errorf("marshal spec to yaml: %w", err)
	}

	if err := os.WriteFile(yamlOutputPath, yamlData, specFilePerm); err != nil {
		return fmt.Errorf("write yaml spec: %w", err)
	}

	return nil
}

// runEmitTypes analyzes all routes and rewrites the generated instances file so the spec build can
// reflect over every model type the handlers reference
func runEmitTypes() error {
	names, err := server.CollectSpecTypes()
	if err != nil {
		return fmt.Errorf("collect spec types: %w", err)
	}

	outputDir, err := specsOutputDir()
	if err != nil {
		return fmt.Errorf("determine specs output directory: %w", err)
	}

	content, err := renderInstancesFile(names)
	if err != nil {
		return err
	}

	if err := os.WriteFile(filepath.Join(outputDir, instancesFilename), []byte(content), specFilePerm); err != nil {
		return fmt.Errorf("write instances file: %w", err)
	}

	return nil
}

// renderInstancesFile renders the generated Go source mapping qualified type names to instances
func renderInstancesFile(names []string) (string, error) {
	aliases := make(map[string]string)
	imports := make([]string, 0)

	var instances strings.Builder

	for _, name := range names {
		dot := strings.LastIndex(name, ".")
		if dot < 0 {
			return "", fmt.Errorf("%w: %q", ErrUnexpectedTypeName, name)
		}

		pkgPath, typeName := name[:dot], name[dot+1:]

		alias, ok := aliases[pkgPath]
		if !ok {
			alias = fmt.Sprintf("pkg%d", len(aliases))
			aliases[pkgPath] = alias
			imports = append(imports, fmt.Sprintf("\t%s %q", alias, pkgPath))
		}

		fmt.Fprintf(&instances, "\t%q: %s.%s{},\n", name, alias, typeName)
	}

	sort.Strings(imports)

	return fmt.Sprintf(`// Code generated by go run ./internal/httpserve/specs/generate --emit-types. DO NOT EDIT.
package specs

import (
%s
)

// SpecInstances maps qualified model type names to instances for schema reflection during spec generation
var SpecInstances = map[string]any{
%s}
`, strings.Join(imports, "\n"), instances.String()), nil
}

func specsOutputDir() (string, error) {
	_, currentFile, _, ok := runtime.Caller(0)
	if !ok {
		return "", server.ErrFailedToGetFilePath
	}

	// Current file is .../internal/httpserve/specs/generate/main.go, so go up two directories
	return filepath.Dir(filepath.Dir(currentFile)), nil
}
