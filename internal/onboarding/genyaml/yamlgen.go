//go:build ignore

package main

import (
	"bytes"
	"context"
	"fmt"
	"go/format"
	"os"
	"sort"

	"github.com/dave/jennifer/jen"
	yaml "github.com/goccy/go-yaml"
	"github.com/urfave/cli/v3"
)

const modelsPkg = "github.com/theopenlane/core/common/models"

type questionnaire struct {
	Version string `yaml:"version"`
	Steps   []step `yaml:"steps"`
}

type step struct {
	Key            string     `yaml:"key"`
	Title          string     `yaml:"title"`
	Description    string     `yaml:"description"`
	Order          int        `yaml:"order"`
	Hidden         bool       `yaml:"hidden"`
	Questions      []question `yaml:"questions"`
	Modules        []module   `yaml:"modules"`
	DynamicModules bool       `yaml:"dynamicModules"`
	Tasks          []taskRule `yaml:"tasks"`
}

type question struct {
	Key            string           `yaml:"key"`
	Label          string           `yaml:"label"`
	Description    string           `yaml:"description"`
	InputType      string           `yaml:"inputType"`
	Format         string           `yaml:"format"`
	CheckboxLabel  string           `yaml:"checkboxLabel"`
	Required       bool             `yaml:"required"`
	Hidden         bool             `yaml:"hidden"`
	DependsOn      *dependency      `yaml:"dependsOn"`
	DynamicOptions bool             `yaml:"dynamicOptions"`
	Options        []questionOption    `yaml:"options"`
	Tasks          map[string]taskRule `yaml:"tasks"`
}

type dependency struct {
	Key    string `yaml:"key"`
	Equals any    `yaml:"equals"`
}

type module struct {
	Key         string `yaml:"key"`
	Title       string `yaml:"title"`
	Description string `yaml:"description"`
}

type questionOption struct {
	Value       string `yaml:"value"`
	Label       string `yaml:"label"`
	Description string `yaml:"description"`
	LogoURL     string `yaml:"logoUrl"`
	Priority    int    `yaml:"priority"`
	Hidden      bool   `yaml:"hidden"`
}

type taskRule struct {
	Key                string `yaml:"key"`
	Title              string `yaml:"title"`
	Details            string `yaml:"details"`
	Priority           int    `yaml:"priority"`
	AvailableAfterDays int    `yaml:"availableAfterDays"`
	Metadata           map[string]any `yaml:"metadata"`
}

func main() {
	if err := app().Run(context.Background(), os.Args); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

func app() *cli.Command {
	return &cli.Command{
		Name:  "genonboarding",
		Usage: "Generate Go source from onboarding YAML",
		Flags: []cli.Flag{
			&cli.StringFlag{
				Name:    "in",
				Value:   "onboarding.yaml",
				Usage:   "Path to the onboarding YAML file",
				Sources: cli.EnvVars("OPENLANE_ONBOARDING_FILE"),
			},
			&cli.StringFlag{
				Name:    "out",
				Value:   "onboarding_gen.go",
				Usage:   "Output Go file",
				Sources: cli.EnvVars("OPENLANE_ONBOARDING_OUTPUT"),
			},
		},
		Action: func(_ context.Context, c *cli.Command) error {
			data, err := os.ReadFile(c.String("in"))
			if err != nil {
				return err
			}

			var parsed questionnaire
			if err := yaml.Unmarshal(data, &parsed); err != nil {
				return err
			}

			if err := validateQuestionnaire(parsed); err != nil {
				return err
			}

			f := jen.NewFile("onboarding")
			f.HeaderComment("Code generated by onboarding yamlgen; DO NOT EDIT.")
			f.Var().Id("defaultQuestionnaire").Op("=").Add(questionnaireLit(parsed))

			buf := &bytes.Buffer{}
			if err := f.Render(buf); err != nil {
				return err
			}

			formatted, err := format.Source(buf.Bytes())
			if err != nil {
				return err
			}

			if c.String("out") == "" {
				_, err = os.Stdout.Write(formatted)
				return err
			}

			return os.WriteFile(c.String("out"), formatted, 0600) //nolint:mnd
		},
	}
}

func questionnaireLit(questionnaire questionnaire) *jen.Statement {
	return jen.Qual(modelsPkg, "Questionnaire").Values(jen.Dict{
		jen.Id("Version"): jen.Lit(questionnaire.Version),
		jen.Id("Steps"):   jen.Index().Qual(modelsPkg, "Step").Values(stepLits(questionnaire.Steps)...),
	})
}

func stepLits(steps []step) []jen.Code {
	out := make([]jen.Code, 0, len(steps))
	for _, step := range steps {
		out = append(out, stepLit(step))
	}

	return out
}

func stepLit(step step) jen.Code {
	fields := jen.Dict{
		jen.Id("Key"):   jen.Lit(step.Key),
		jen.Id("Title"): jen.Lit(step.Title),
		jen.Id("Order"): jen.Lit(step.Order),
	}

	if step.Description != "" {
		fields[jen.Id("Description")] = jen.Lit(step.Description)
	}

	if step.Hidden {
		fields[jen.Id("Hidden")] = jen.True()
	}

	fields[jen.Id("Questions")] = jen.Index().Qual(modelsPkg, "Question").Values(questionLits(step.Questions)...)

	if len(step.Modules) > 0 {
		fields[jen.Id("Modules")] = jen.Index().Qual(modelsPkg, "Module").Values(moduleLits(step.Modules)...)
	}

	if step.DynamicModules {
		fields[jen.Id("DynamicModules")] = jen.True()
	}

	if len(step.Tasks) > 0 {
		fields[jen.Id("Tasks")] = jen.Index().Qual(modelsPkg, "TaskRule").Values(taskRuleLits(step.Tasks)...)
	}

	return jen.Qual(modelsPkg, "Step").Values(fields)
}

func questionLits(questions []question) []jen.Code {
	out := make([]jen.Code, 0, len(questions))
	for _, question := range questions {
		out = append(out, questionLit(question))
	}

	return out
}

func questionLit(question question) jen.Code {
	fields := jen.Dict{
		jen.Id("Key"):       jen.Lit(question.Key),
		jen.Id("Label"):     jen.Lit(question.Label),
		jen.Id("InputType"): jen.Qual(modelsPkg, getInputType(question.InputType)),
	}

	if question.Description != "" {
		fields[jen.Id("Description")] = jen.Lit(question.Description)
	}

	if question.Format != "" {
		fields[jen.Id("Format")] = jen.Lit(question.Format)
	}

	if question.CheckboxLabel != "" {
		fields[jen.Id("CheckboxLabel")] = jen.Lit(question.CheckboxLabel)
	}

	if question.Required {
		fields[jen.Id("Required")] = jen.True()
	}

	if question.Hidden {
		fields[jen.Id("Hidden")] = jen.True()
	}

	if question.DependsOn != nil {
		fields[jen.Id("DependsOn")] = jen.Op("&").Qual(modelsPkg, "QuestionDependency").Values(jen.Dict{
			jen.Id("Key"):    jen.Lit(question.DependsOn.Key),
			jen.Id("Equals"): literal(question.DependsOn.Equals),
		})
	}

	if question.DynamicOptions {
		fields[jen.Id("DynamicOptions")] = jen.True()
	}

	if len(question.Options) > 0 {
		fields[jen.Id("Options")] = jen.Index().Qual(modelsPkg, "QuestionOption").Values(optionLits(question.Options)...)
	}

	if len(question.Tasks) > 0 {
		fields[jen.Id("Tasks")] = taskRuleMap(question.Tasks)
	}

	return jen.Qual(modelsPkg, "Question").Values(fields)
}

func validateQuestionnaire(questionnaire questionnaire) error {
	for _, step := range questionnaire.Steps {
		for _, question := range step.Questions {
			if getInputType(question.InputType) == "" {
				return fmt.Errorf("question %q has unsupported inputType %q", question.Key, question.InputType)
			}
		}
	}

	return nil
}

func getInputType(typ string) string {
	switch typ {
	case "string":
		return "InputTypeString"
	case "boolean":
		return "InputTypeBoolean"
	case "checkbox":
		return "InputTypeCheckbox"
	case "select":
		return "InputTypeSelect"
	case "multiselect":
		return "InputTypeMultiselect"
	case "multi-input":
		return "InputTypeMultiInput"
	default:
		return ""
	}
}

func moduleLits(modules []module) []jen.Code {
	out := make([]jen.Code, 0, len(modules))
	for _, module := range modules {
		out = append(out, jen.Qual(modelsPkg, "Module").Values(jen.Dict{
			jen.Id("Key"):         jen.Lit(module.Key),
			jen.Id("Title"):       jen.Lit(module.Title),
			jen.Id("Description"): jen.Lit(module.Description),
		}))
	}

	return out
}

func literal(value any) jen.Code {
	switch v := value.(type) {
	case bool:
		return jen.Lit(v)
	case int:
		return jen.Lit(v)
	case float64:
		return jen.Lit(v)
	case []any:
		return jen.Index().Any().ValuesFunc(func(g *jen.Group) {
			for _, item := range v {
				g.Add(literal(item))
			}
		})
	case map[string]any:
		return metadataLit(v)
	case string:
		return jen.Lit(v)
	default:
		return jen.Lit(fmt.Sprint(v))
	}
}

func taskRuleLits(rules []taskRule) []jen.Code {
	out := make([]jen.Code, 0, len(rules))
	for _, rule := range rules {
		out = append(out, taskRuleLit(rule))
	}

	return out
}

func taskRuleMap(rules map[string]taskRule) jen.Code {
	keys := make([]string, 0, len(rules))
	for key := range rules {
		keys = append(keys, key)
	}
	sort.Strings(keys)

	return jen.Map(jen.String()).Qual(modelsPkg, "TaskRule").ValuesFunc(func(g *jen.Group) {
		for _, key := range keys {
			g.Lit(key).Op(":").Add(taskRuleLit(rules[key]))
		}
	})
}

func taskRuleLit(rule taskRule) jen.Code {
	fields := jen.Dict{
		jen.Id("Key"):      jen.Lit(rule.Key),
		jen.Id("Title"):    jen.Lit(rule.Title),
		jen.Id("Details"):  jen.Lit(rule.Details),
		jen.Id("Priority"): jen.Lit(rule.Priority),
	}

	if rule.AvailableAfterDays > 0 {
		fields[jen.Id("AvailableAfterDays")] = jen.Lit(rule.AvailableAfterDays)
	}

	if len(rule.Metadata) > 0 {
		fields[jen.Id("Metadata")] = metadataLit(rule.Metadata)
	}

	return jen.Qual(modelsPkg, "TaskRule").Values(fields)
}

func metadataLit(metadata map[string]any) jen.Code {
	keys := make([]string, 0, len(metadata))
	for key := range metadata {
		keys = append(keys, key)
	}
	sort.Strings(keys)

	return jen.Map(jen.String()).Any().ValuesFunc(func(g *jen.Group) {
		for _, key := range keys {
			g.Lit(key).Op(":").Add(literal(metadata[key]))
		}
	})
}

func optionLits(options []questionOption) []jen.Code {
	out := make([]jen.Code, 0, len(options))
	for _, option := range options {
		fields := jen.Dict{
			jen.Id("Value"): jen.Lit(option.Value),
			jen.Id("Label"): jen.Lit(option.Label),
		}

		if option.Description != "" {
			fields[jen.Id("Description")] = jen.Lit(option.Description)
		}

		if option.LogoURL != "" {
			fields[jen.Id("LogoURL")] = jen.Lit(option.LogoURL)
		}

		if option.Priority > 0 {
			fields[jen.Id("Priority")] = jen.Lit(option.Priority)
		}

		if option.Hidden {
			fields[jen.Id("Hidden")] = jen.True()
		}

		out = append(out, jen.Qual(modelsPkg, "QuestionOption").Values(fields))
	}

	return out
}
