//go:build cli

package scheduledjob

import (
	"embed"
	"fmt"
	"reflect"

	"github.com/theopenlane/core/cmd/cli/internal/speccli"
	"github.com/theopenlane/core/pkg/openlaneclient"
)

//go:embed spec.json
var specFS embed.FS

func init() {
	typeMap := map[string]reflect.Type{
		"openlaneclient.CreateScheduledJobInput": reflect.TypeOf(openlaneclient.CreateScheduledJobInput{}),
		"openlaneclient.UpdateScheduledJobInput": reflect.TypeOf(openlaneclient.UpdateScheduledJobInput{}),
	}

	if err := speccli.RegisterFromFS(specFS, speccli.LoaderOptions{
		TypeResolver: speccli.StaticTypeResolver(typeMap),
		Parsers:      speccli.DefaultParsers(),
	}); err != nil {
		panic(fmt.Sprintf("failed to register scheduledjob command: %v", err))
	}
}
