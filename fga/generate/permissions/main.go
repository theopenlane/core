//go:build fgagenerate

// Command gen-fga-permissions writes a JSON catalog of the organization-type relations in the
// assembled FGA model — the source of truth the frontend codegen turns into its permission enum.
package main

import (
	"encoding/json"
	"errors"
	"flag"
	"log"
	"os"
	"path/filepath"
	"slices"
	"strings"

	language "github.com/openfga/language/pkg/go/transformer"
)

// organizationType is the FGA type whose relations back the org-level permission checks.
const organizationType = "organization"

// catalog is the generated artifact consumed by downstream code generators.
type catalog struct {
	Comment      string   `json:"_comment"`
	Organization []string `json:"organization"`
}

func main() {
	var (
		modFile = flag.String("mod", "fga/model/fga.mod", "path to the FGA module manifest")
		outFile = flag.String("out", "fga/model/generated/permissions.json", "output JSON catalog")
	)
	flag.Parse()

	relations, err := organizationRelations(*modFile)
	if err != nil {
		log.Fatalf("collecting organization relations: %v", err)
	}

	out := catalog{
		Comment:      "Code generated by gen-fga-permissions; DO NOT EDIT. Source: fga/model/fga.mod.",
		Organization: relations,
	}

	data, err := json.MarshalIndent(out, "", "  ")
	if err != nil {
		log.Fatalf("marshaling catalog: %v", err)
	}
	data = append(data, '\n')

	if err := os.MkdirAll(filepath.Dir(*outFile), 0o755); err != nil {
		log.Fatalf("creating output dir: %v", err)
	}
	if err := os.WriteFile(*outFile, data, 0o644); err != nil {
		log.Fatalf("writing %s: %v", *outFile, err)
	}

	log.Printf("generated %s with %d organization relation(s)", *outFile, len(relations))
}

// organizationRelations assembles the modular model and returns the organization type's sorted relations.
func organizationRelations(modPath string) ([]string, error) {
	modData, err := os.ReadFile(modPath)
	if err != nil {
		return nil, err
	}

	mod, err := language.TransformModFile(string(modData))
	if err != nil {
		return nil, err
	}

	baseDir := filepath.Dir(modPath)
	modules := make([]language.ModuleFile, 0, len(mod.Contents.Value))
	for _, entry := range mod.Contents.Value {
		rel := entry.Value
		contents, readErr := os.ReadFile(filepath.Join(baseDir, rel))
		if readErr != nil {
			return nil, readErr
		}
		modules = append(modules, language.ModuleFile{Name: rel, Contents: string(contents)})
	}

	model, err := language.TransformModuleFilesToModel(modules, mod.Schema.Value)
	if err != nil {
		return nil, err
	}

	for _, td := range model.GetTypeDefinitions() {
		if !strings.EqualFold(td.GetType(), organizationType) {
			continue
		}

		relations := make([]string, 0, len(td.GetRelations()))
		for name := range td.GetRelations() {
			relations = append(relations, name)
		}
		slices.Sort(relations)

		return relations, nil
	}

	return nil, errNoOrganizationType
}

var errNoOrganizationType = errors.New("organization type not found in fga model")
