//go:build ignore

package main

import (
	"bytes"
	"cmp"
	"flag"
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"log"
	"os"
	"path/filepath"
	"slices"
	"strings"

	"github.com/stoewer/go-strcase"
)

// parentObjectMap is used to add additional object types to the parent type
// fore example, having edit to a group should also allow edit access to the group settings and group membership, but these are not separate schemas in ent, so they would not be picked up by the generator without this map
var parentObjectMap = map[string][]string{
	"group_setting":        {"group"},
	"group_membership":     {"group"},
	"organization_setting": {"organization"},
	"org_membership":       {"organization"},
	"program_membership":   {"program"},
	"subcontrol":           {"control"},
}

// shcemaInfo holds information about an ent schema that is relevant for generating FGA permissions, such as the name of the schema and whether it has create access rules defined in its Policy function.
type schemaInfo struct {
	// name is the name of the schema, e.g. "User", "Group", etc.
	name string
	// canCreateAccess indicates whether the schema allows for group based object creation
	canCreateAccess bool
	// canCreateServiceOnly indicates whether the schema allows for service-only creation access (i.e. only tokens with service scope can create the object, users cannot create the object even if they have edit access)
	canCreateServiceOnly bool
}

const (
	// checkServiceCreateAccess is the name of the function in the policy that checks if the user has service-level create access
	checkServiceCreateAccess = "CheckServiceCreateAccess"
	// checkCreateAccess is the name of the function in the policy that checks if the user has create access (either service or user)
	checkCreateAccess = "CheckCreateAccess"
)

func main() {
	var (
		schemaDir = flag.String("schema-dir", "internal/ent/schema", "path to ent schema directory")
		outFile   = flag.String("out", "fga/model/generated/crud.fga", "output FGA file")
	)
	flag.Parse()

	types, err := findAllSchemas(*schemaDir)
	if err != nil {
		log.Fatalf("finding schemas: %v", err)
	}

	slices.SortFunc(types, func(a, b schemaInfo) int {
		return cmp.Compare(a.name, b.name)
	})

	var buf bytes.Buffer
	buf.WriteString("// Code generated by gen-fga-org-crud; DO NOT EDIT.\n\n")
	buf.WriteString("module base\n")
	buf.WriteString("extend type organization\n")
	buf.WriteString("  relations\n")

	// define the types for each schema cRUD
	for i, typeName := range types {
		fgaType := strcase.SnakeCase(typeName.name)

		// all schemas can. have a basic view, edit, delete permission set, this does not imply that the
		// schema uses these definitions in practice, just that they are defined in the model and available for use in the schema's policy
		canViewRelations := []string{fmt.Sprintf("can_edit_%s", fgaType)}
		canEditRelations := []string{fmt.Sprintf("can_delete_%s", fgaType)}
		creatorRelations := []string{fmt.Sprintf("%s_creator", fgaType)}
		additionalRelations, ok := parentObjectMap[fgaType]
		if ok {
			for _, r := range additionalRelations {
				canViewRelations = append(canViewRelations, fmt.Sprintf("can_view_%s", r))
				canEditRelations = append(canEditRelations, fmt.Sprintf("can_edit_%s", r))
			}
		}

		buf.WriteString(fmt.Sprintf("    define can_view_%s: [service, user] or %s\n", fgaType, strings.Join(canViewRelations, " or ")))
		buf.WriteString(fmt.Sprintf("    define can_edit_%s: [service, user] or %s\n", fgaType, strings.Join(canEditRelations, " or ")))
		buf.WriteString(fmt.Sprintf("    define can_delete_%s: [service, user]\n", fgaType))

		if typeName.canCreateAccess {
			buf.WriteString(fmt.Sprintf("    define %s_creator: [group#member]\n", fgaType))
			buf.WriteString(fmt.Sprintf("    define can_create_%s: can_edit or %s\n", fgaType, strings.Join(creatorRelations, " or ")))
		} else if typeName.canCreateServiceOnly {
			buf.WriteString(fmt.Sprintf("    define can_create_%s: [service]\n", fgaType))
		}

		if i < len(types)-1 {
			buf.WriteString("\n")
		}
	}

	if err := os.MkdirAll(filepath.Dir(*outFile), 0o755); err != nil {
		log.Fatalf("creating output dir: %v", err)
	}
	if err := os.WriteFile(*outFile, buf.Bytes(), 0o644); err != nil {
		log.Fatalf("writing output file: %v", err)
	}

	log.Printf("generated %s with %d type(s)", *outFile, len(types))
}

// findAllSchemas looks for all checks in the ent schemas directory in order to get all schemas
// and determine the organization level CRUD access for each schema based on the presence of CheckCreateAccess and CheckServiceCreateAccess in the Policy function for each schema.
func findAllSchemas(schemaDir string) ([]schemaInfo, error) {
	entries, err := goFilesInDir(schemaDir)
	if err != nil {
		return nil, fmt.Errorf("reading schema directory: %w", err)
	}

	log.Printf("Checking all %d schemas", len(entries))

	schemas := []schemaInfo{}

	for _, file := range entries {
		fset := token.NewFileSet()
		parsedFile, err := parser.ParseFile(fset, file, nil, parser.ParseComments)
		if err != nil {
			return nil, fmt.Errorf("parsing file %s: %w", file, err)
		}

		schema := schemaInfo{}

		// not all schemas have policies, but they all should have the Name function which we can use to get the name of the schema and use that as the object type in FGA
		for _, decl := range parsedFile.Decls {
			fn, ok := decl.(*ast.FuncDecl)
			if !ok {
				continue
			}

			if fn.Name == nil || fn.Name.Name != "Name" {
				continue
			}

			recvName, ok := receiverTypeName(fn)
			if !ok {
				continue
			}

			schema.name = recvName
			break
		}

		// if the schema has a Policy function, we can check for the presence of CheckCreateAccess and CheckServiceCreateAccess to determine the create access level for the schema
		for _, decl := range parsedFile.Decls {
			fn, ok := decl.(*ast.FuncDecl)
			if !ok {
				continue
			}

			if fn.Name == nil || fn.Name.Name != "Policy" {
				continue
			}

			if fn.Body == nil {
				continue
			}

			schema.functionContainsCheckCreateAccess(fn.Body)
		}

		if schema.name != "" {
			schemas = append(schemas, schema)
		}
	}

	return schemas, nil
}

func goFilesInDir(dir string) ([]string, error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil, err
	}

	var files []string
	for _, e := range entries {
		if e.IsDir() {
			continue
		}

		if filepath.Ext(e.Name()) == ".go" && !strings.HasSuffix(e.Name(), "_test.go") {
			files = append(files, filepath.Join(dir, e.Name()))
		}
	}

	return files, nil
}

func receiverTypeName(fn *ast.FuncDecl) (string, bool) {
	if fn.Recv == nil || len(fn.Recv.List) != 1 {
		return "", false
	}

	t := fn.Recv.List[0].Type
	switch rt := t.(type) {
	case *ast.Ident:
		return rt.Name, true
	case *ast.StarExpr:
		if id, ok := rt.X.(*ast.Ident); ok {
			return id.Name, true
		}
	}

	return "", false
}

func (s *schemaInfo) functionContainsCheckCreateAccess(body *ast.BlockStmt) {
	ast.Inspect(body, func(n ast.Node) bool {
		if n == nil {
			return false
		}

		call, ok := n.(*ast.CallExpr)
		if !ok {
			return true
		}

		sel, ok := call.Fun.(*ast.SelectorExpr)
		if !ok {
			return true
		}

		pkgIdent, ok := sel.X.(*ast.Ident)
		if !ok {
			return true
		}

		if pkgIdent.Name == "policy" && sel.Sel != nil && sel.Sel.Name == checkServiceCreateAccess {
			s.canCreateServiceOnly = true
			s.canCreateAccess = false
			return false

		}

		if pkgIdent.Name == "policy" && sel.Sel != nil && sel.Sel.Name == checkCreateAccess {
			s.canCreateServiceOnly = false
			s.canCreateAccess = true
			return false
		}

		return true
	})
}
