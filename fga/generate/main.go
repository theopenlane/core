var parentObjectMap = map[string][]string{
	"group_setting":        {"group"},
	"group_membership":     {"group"},
	"organization_setting": {"organization"},
	"org_membership":       {"organization"},
	"program_membership":   {"program"},
	"subcontrol":           {"control"},
}

type schemaInfo struct {
	name                 string
	canCreateAccess      bool
	canCreateServiceOnly bool
}

const (
	checkServiceCreateAccess = "CheckServiceCreateAccess"
	checkCreateAccess        = "CheckCreateAccess"
)

func main() {
	var (
		schemaDir = flag.String("schema-dir", "internal/ent/schema", "path to ent schema directory")
		rolesFile = flag.String("roles-file", "fga/model/roles/roles.fga", "path to FGA roles file for parsing additional CRUD access annotations")
		outFile   = flag.String("out", "fga/model/generated/crud.fga", "output FGA file")
	)
	flag.Parse()

	types, err := findAllSchemas(*schemaDir)
	if err != nil {
		log.Fatalf("finding schemas: %v", err)
	}
	slices.SortFunc(types, func(a, b schemaInfo) int {
		return cmp.Compare(a.name, b.name)
	})

	crudMap, viewMap, inheritMap, createMap := parseRoleAnnotations(*rolesFile)

	fmt.Println(inheritMap)

	for role, inherited := range inheritMap {
		for _, in := range inherited {
			// Propagate for all objects managed by the inherited role
			for obj := range crudMap {
				for _, r := range crudMap[obj] {
					if r == in {
						crudMap[obj] = append(crudMap[obj], role)
						break
					}
				}
			}
			for obj := range viewMap {
				for _, r := range viewMap[obj] {
					if r == in {
						viewMap[obj] = append(viewMap[obj], role)
						break
					}
				}
			}
			for obj := range createMap {
				for _, r := range createMap[obj] {
					if r == in {
						createMap[obj] = append(createMap[obj], role)
						break
					}
				}
			}
		}
	}

	var buf bytes.Buffer
	buf.WriteString("// Code generated by gen-fga-org-crud; DO NOT EDIT.\n\n")
	buf.WriteString("module base\n")
	buf.WriteString("extend type organization\n")
	buf.WriteString("  relations\n")

	for i, typeName := range types {
		fgaType := strcase.SnakeCase(typeName.name)

		// Only use roles directly below the annotation (no inheritance)
		roleSet := make(map[string]struct{})
		for _, role := range crudMap[fgaType] {
			roleSet[role] = struct{}{}
		}
		viewRoleSet := make(map[string]struct{})
		for _, role := range viewMap[fgaType] {
			viewRoleSet[role] = struct{}{}
		}
		createRoleSet := make(map[string]struct{})
		for _, role := range createMap[fgaType] {
			createRoleSet[role] = struct{}{}
		}
		canViewRelations := []string{fmt.Sprintf("can_edit_%s", fgaType)}
		canEditRelations := []string{fmt.Sprintf("can_delete_%s", fgaType)}
		canDeleteRelations := []string{}
		creatorRelations := []string{fmt.Sprintf("%s_creator", fgaType)}
		canCreateRelations := []string{}

		for r := range roleSet {
			canViewRelations = append(canViewRelations, r)
			canEditRelations = append(canEditRelations, r)
			canDeleteRelations = append(canDeleteRelations, r)
			creatorRelations = append(creatorRelations, r)
		}
		for r := range viewRoleSet {
			canViewRelations = append(canViewRelations, r)
		}
		for r := range createRoleSet {
			canCreateRelations = append(canCreateRelations, r)
		}

		// Parent relations use can_manage_ prefix
		if additionalRelations, ok := parentObjectMap[fgaType]; ok {
			for _, r := range additionalRelations {
				canViewRelations = append(canViewRelations, fmt.Sprintf("can_view_%s", r))
				canEditRelations = append(canEditRelations, fmt.Sprintf("can_edit_%s", r))
				canDeleteRelations = append(canDeleteRelations, fmt.Sprintf("can_delete_%s", r))
				creatorRelations = append(creatorRelations, fmt.Sprintf("%s_creator", r))
			}
		}

		buf.WriteString(fmt.Sprintf("    define can_view_%s: [service, user] or %s\n", fgaType, strings.Join(canViewRelations, " or ")))
		buf.WriteString(fmt.Sprintf("    define can_edit_%s: [service, user] or %s\n", fgaType, strings.Join(canEditRelations, " or ")))
		if len(canDeleteRelations) > 0 {
			buf.WriteString(fmt.Sprintf("    define can_delete_%s: [service, user] or %s\n", fgaType, strings.Join(canDeleteRelations, " or ")))
		} else {
			buf.WriteString(fmt.Sprintf("    define can_delete_%s: [service, user]\n", fgaType))
		}
		if typeName.canCreateAccess {
			buf.WriteString(fmt.Sprintf("    define %s_creator: [group#member]\n", fgaType))
		}

		var createExpr []string
		if typeName.canCreateAccess {
			createExpr = append(createExpr, "can_edit", fmt.Sprintf("%s_creator", fgaType))
		}
		for r := range createRoleSet {
			createExpr = append(createExpr, r)
		}
		// Add manager roles for this object (including inherited ones)
		for r := range roleSet {
			createExpr = append(createExpr, r)
		}
		if typeName.canCreateServiceOnly && len(createExpr) == 0 {
			createExpr = append(createExpr, "[service]")
		}
		if len(createExpr) > 0 {
			buf.WriteString(fmt.Sprintf("    define can_create_%s: %s\n", fgaType, strings.Join(createExpr, " or ")))
		}

		if i < len(types)-1 {
			buf.WriteString("\n")
		}
	}

	if err := os.MkdirAll(filepath.Dir(*outFile), 0o755); err != nil {
		log.Fatalf("creating output dir: %v", err)
	}
	if err := os.WriteFile(*outFile, buf.Bytes(), 0o644); err != nil {
		log.Fatalf("writing output file: %v", err)
	}
	log.Printf("generated %s with %d type(s)", *outFile, len(types))
}

// collectInherited is now unused, but kept for compatibility.
func collectInherited(role string, inheritMap map[string][]string) map[string]struct{} {
	roles := map[string]struct{}{role: {}}
	for _, in := range inheritMap[role] {
		roles[in] = struct{}{}
	}
	return roles
}

// parseRoleAnnotations parses relevant annotations and role lines, including @create.
func parseRoleAnnotations(rolesFile string) (map[string][]string, map[string][]string, map[string][]string, map[string][]string) {
	data, _ := os.ReadFile(rolesFile)
	lines := strings.Split(string(data), "\n")
	crudMap := make(map[string][]string)
	viewMap := make(map[string][]string)
	inheritMap := make(map[string][]string)
	createMap := make(map[string][]string)
	var pendingCrud, pendingView, pendingInherit, pendingCreate []string
	for _, line := range lines {
		line = strings.TrimSpace(line)
		if strings.HasPrefix(line, "# @crud:") {
			pendingCrud = strings.Split(strings.TrimPrefix(line, "# @crud:"), ",")
			for i := range pendingCrud {
				pendingCrud[i] = strings.TrimSpace(pendingCrud[i])
			}
		}
		if strings.HasPrefix(line, "# @view:") {
			pendingView = strings.Split(strings.TrimPrefix(line, "# @view:"), ",")
			for i := range pendingView {
				pendingView[i] = strings.TrimSpace(pendingView[i])
			}
		}
		if strings.HasPrefix(line, "# @inherit:") {
			pendingInherit = strings.Split(strings.TrimPrefix(line, "# @inherit:"), ",")
			for i := range pendingInherit {
				pendingInherit[i] = strings.TrimSpace(pendingInherit[i])
			}
		}
		if strings.HasPrefix(line, "# @create:") {
			pendingCreate = strings.Split(strings.TrimPrefix(line, "# @create:"), ",")
			for i := range pendingCreate {
				pendingCreate[i] = strings.TrimSpace(pendingCreate[i])
			}
		}
		if strings.HasPrefix(line, "define ") {
			parts := strings.Fields(line)
			if len(parts) > 1 {
				role := strings.TrimSuffix(parts[1], ":")
				for _, obj := range pendingCrud {
					if obj != "" {
						crudMap[obj] = append(crudMap[obj], role)
					}
				}
				for _, obj := range pendingView {
					if obj != "" {
						viewMap[obj] = append(viewMap[obj], role)
					}
				}
				for _, in := range pendingInherit {
					if in != "" {
						inheritMap[role] = append(inheritMap[role], in)
					}
				}
				for _, obj := range pendingCreate {
					if obj != "" {
						createMap[obj] = append(createMap[obj], role)
					}
				}
			}
			pendingCrud = nil
			pendingView = nil
			pendingInherit = nil
			pendingCreate = nil
		}
	}
	return crudMap, viewMap, inheritMap, createMap
}

// findAllSchemas looks for all checks in the ent schemas directory in order to get all schemas
// and determine the organization level CRUD access for each schema based on the presence of CheckCreateAccess and CheckServiceCreateAccess in the Policy function for each schema.
func findAllSchemas(schemaDir string) ([]schemaInfo, error) {
	entries, err := goFilesInDir(schemaDir)
	if err != nil {
		return nil, fmt.Errorf("reading schema directory: %w", err)
	}

	log.Printf("Checking all %d schemas", len(entries))

	schemas := []schemaInfo{}

	for _, file := range entries {
		fset := token.NewFileSet()
		parsedFile, err := parser.ParseFile(fset, file, nil, parser.ParseComments)
		if err != nil {
			return nil, fmt.Errorf("parsing file %s: %w", file, err)
		}

		schema := schemaInfo{}

		// not all schemas have policies, but they all should have the Name function which we can use to get the name of the schema and use that as the object type in FGA
		for _, decl := range parsedFile.Decls {
			fn, ok := decl.(*ast.FuncDecl)
			if !ok {
				continue
			}

			if fn.Name == nil || fn.Name.Name != "Name" {
				continue
			}

			recvName, ok := receiverTypeName(fn)
			if !ok {
				continue
			}

			schema.name = recvName
			log.Printf("Schema found: %s", schema.name)

			break
		}

		// if the schema has a Policy function, we can check for the presence of CheckCreateAccess and CheckServiceCreateAccess to determine the create access level for the schema
		for _, decl := range parsedFile.Decls {
			fn, ok := decl.(*ast.FuncDecl)
			if !ok {
				continue
			}

			if fn.Name == nil || fn.Name.Name != "Policy" {
				continue
			}

			if fn.Body == nil {
				continue
			}

			schema.functionContainsCheckCreateAccess(fn.Body)

			log.Printf("Schema %s canCreateAccess: %v", schema.name, schema.canCreateAccess)
		}

		if schema.name != "" {
			schemas = append(schemas, schema)
		}
	}

	return schemas, nil
}

func goFilesInDir(dir string) ([]string, error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil, err
	}

	var files []string
	for _, e := range entries {
		if e.IsDir() {
			continue
		}

		if filepath.Ext(e.Name()) == ".go" && !strings.HasSuffix(e.Name(), "_test.go") {
			files = append(files, filepath.Join(dir, e.Name()))
		}
	}

	return files, nil
}

func receiverTypeName(fn *ast.FuncDecl) (string, bool) {
	if fn.Recv == nil || len(fn.Recv.List) != 1 {
		return "", false
	}

	t := fn.Recv.List[0].Type
	switch rt := t.(type) {
	case *ast.Ident:
		return rt.Name, true
	case *ast.StarExpr:
		if id, ok := rt.X.(*ast.Ident); ok {
			return id.Name, true
		}
	}

	return "", false
}

func (s *schemaInfo) functionContainsCheckCreateAccess(body *ast.BlockStmt) {
	ast.Inspect(body, func(n ast.Node) bool {
		if n == nil {
			return false
		}

		call, ok := n.(*ast.CallExpr)
		if !ok {
			return true
		}

		sel, ok := call.Fun.(*ast.SelectorExpr)
		if !ok {
			return true
		}

		pkgIdent, ok := sel.X.(*ast.Ident)
		if !ok {
			return true
		}

		if pkgIdent.Name == "policy" && sel.Sel != nil && sel.Sel.Name == checkServiceCreateAccess {
			s.canCreateServiceOnly = true
			s.canCreateAccess = false
			return false

		}

		if pkgIdent.Name == "policy" && sel.Sel != nil && sel.Sel.Name == checkCreateAccess {
			s.canCreateServiceOnly = false
			s.canCreateAccess = true
			return false
		}

		return true
	})
}
