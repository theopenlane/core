//go:build fgagenerate

package main

import (
	"bytes"
	"flag"
	"fmt"
	"log"
	"os"
	"path/filepath"
	"slices"
	"strings"

	"github.com/stoewer/go-strcase"
	"github.com/theopenlane/core/fga/generate/modelparse"
	"github.com/theopenlane/core/fga/generate/schemaparse"
)

const (
	// fullAccessRelation is the name of the relation for org owners and super admins
	fullAccessRelation = "full_access"
	// ownerRelation is the name of the relation for only organization owner
	ownerRelation = "owner"
)

const (
	canDeletePrefix         = "can_delete_"
	canEditPrefix           = "can_edit_"
	canViewPrefix           = "can_view_"
	canCreatePrefix         = "can_create_"
	creatorSuffix           = "_creator"
	defaultRelations        = "[service, user, group#member]"
	defaultRelationsNoGroup = "[service, user]"
)

// this generator looks at all the ent schemas and their policy functions to determine the organization-level CRUD access for each schema and generates a crud.fga file that defines the appropriate relations for each schema based on that access. This allows us to have a consistent set of permissions for each schema in FGA that are automatically generated based on the presence of certain rules in the ent policies, which reduces the likelihood of human error in defining permissions and ensures that all schemas have a consistent permission structure in FGA.
func main() {
	var (
		schemaDir = flag.String("schema-dir", "./internal/ent/schema", "path to ent schema directory")
		rolesFile = flag.String("roles-file", "fga/model/roles/roles.fga", "path to FGA roles file for parsing additional CRUD access annotations")
		outFile   = flag.String("out", "fga/model/generated/crud.fga", "output FGA file")
	)
	flag.Parse()

	schemas, err := schemaparse.GetSchemas(*schemaDir)
	if err != nil {
		log.Fatalf("error finding schemas: %v", err)
	}

	roles, err := modelparse.ParseRoleAnnotations(*rolesFile)
	if err != nil {
		log.Fatalf("error parsing role annotations: %v", err)
	}
	roles.AddInheritedRoles()

	var buf bytes.Buffer

	// add headers to the generated file
	buf.WriteString("# Code generated by gen-fga-org-crud; DO NOT EDIT.\n\n")
	buf.WriteString("module base\n")
	buf.WriteString("extend type organization\n")
	buf.WriteString("  relations\n")

	count := 0
	for i, si := range schemas {
		if si.ExcludeFromGeneration {
			fmt.Printf("%s excluded from crud generation, skipping\n", si.Name)
			continue
		}

		fgaType := strcase.SnakeCase(si.Name)

		canViewRelations := []string{}
		if !si.OnlyAllowCreate {
			canViewRelations = append(canViewRelations, getCrudRelation(canEditPrefix, fgaType))
		}

		canEditRelations := []string{getCrudRelation(canDeletePrefix, fgaType)}
		canDeleteRelations := []string{}
		creatorRelations := []string{getCrudRelation(creatorSuffix, fgaType)}

		canViewRelations = append(canViewRelations, roles.CrudRoles[fgaType]...)
		canEditRelations = append(canEditRelations, roles.CrudRoles[fgaType]...)
		canDeleteRelations = append(canDeleteRelations, roles.CrudRoles[fgaType]...)
		creatorRelations = append(creatorRelations, roles.CrudRoles[fgaType]...)

		canViewRelations = append(canViewRelations, roles.ViewRoles[fgaType]...)

		for _, r := range si.Parents {
			canViewRelations = append(canViewRelations, getCrudRelation(canViewPrefix, r))
			canEditRelations = append(canEditRelations, getCrudRelation(canEditPrefix, r))
			canDeleteRelations = append(canDeleteRelations, getCrudRelation(canDeletePrefix, r))

			if si.HasGroupCreator {
				creatorRelations = append(creatorRelations, getCrudRelation(creatorSuffix, r))
			}
		}

		canEditRelations = append(canEditRelations, fullAccessRelation)
		canViewRelations = append(canViewRelations, fullAccessRelation)

		// only org owners can delete the organization
		if !strings.EqualFold(fgaType, "organization") {
			canDeleteRelations = append(canDeleteRelations, fullAccessRelation)
		} else {
			canDeleteRelations = append(canDeleteRelations, ownerRelation)
		}

		// sort the relations to ensure consistent output
		slices.Sort(canViewRelations)
		slices.Sort(canEditRelations)
		slices.Sort(canDeleteRelations)
		slices.Sort(creatorRelations)

		buf.WriteString(getEntry(canViewPrefix, si, canViewRelations...))

		if !si.OnlyAllowCreate {
			buf.WriteString(getEntry(canEditPrefix, si, canEditRelations...))

			buf.WriteString(getEntry(canDeletePrefix, si, canDeleteRelations...))
		}

		if si.HasCreate && si.HasGroupCreator {
			buf.WriteString(fmt.Sprintf("    define %s%s: [group#member]\n", fgaType, creatorSuffix))
		}

		var createExpr []string
		if si.HasCreate {
			// add access for org level can_edit and can_edit_object for the object, this allows api tokens to create objects if they have "write" access to the object type.
			createExpr = append(createExpr, "can_edit")

			if !si.OnlyAllowCreate {
				createExpr = append(createExpr, getCrudRelation(canEditPrefix, fgaType))
			}

			if si.HasGroupCreator {
				createExpr = append(createExpr, getCrudRelation(creatorSuffix, fgaType))
			}
		}

		createExpr = append(createExpr, roles.CreateRoles[fgaType]...)
		createExpr = append(createExpr, roles.CrudRoles[fgaType]...)

		if si.CanCreateServiceOnly && len(createExpr) == 0 {
			createExpr = append(createExpr, "[service]")
		}

		if len(createExpr) > 0 {
			buf.WriteString(fmt.Sprintf("    define %s%s: %s\n", canCreatePrefix, fgaType, strings.Join(createExpr, " or ")))
		}

		if i < len(schemas)-1 {
			buf.WriteString("\n")
		}

		count++
	}

	if err := os.MkdirAll(filepath.Dir(*outFile), 0o755); err != nil {
		log.Fatalf("creating output dir: %v", err)
	}

	if err := os.WriteFile(*outFile, buf.Bytes(), 0o644); err != nil {
		log.Fatalf("writing output file: %v", err)
	}

	log.Printf("generated %s with %d type(s)", *outFile, count)
}

func getCrudRelation(relation, object string) string {
	if strings.HasPrefix("_", relation) {
		// relation is a suffix so return in reverse
		return fmt.Sprintf("%s%s", object, relation)
	}

	return fmt.Sprintf("%s%s", relation, object)
}

func getEntry(relation string, si schemaparse.SchemaInfo, additionalRelations ...string) string {
	relForType := defaultRelations
	if !si.HasGroupCreator {
		relForType = defaultRelationsNoGroup
	}

	object := strcase.SnakeCase(si.Name)
	defTuple := getCrudRelation(relation, object)

	if len(additionalRelations) == 0 {
		return fmt.Sprintf("    define %s: %s\n", defTuple, relForType)
	}

	return fmt.Sprintf("    define %s: %s or %s\n", defTuple, relForType, strings.Join(additionalRelations, " or "))
}
