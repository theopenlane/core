// parentObjectMap is used to add additional object types to the parent type
// for example, having edit to a group should also allow edit access to the group settings and group membership, but these are not separate schemas in ent, so they would not be picked up by the generator without this map
var parentObjectMap = map[string][]string{
	"group_setting":          {"group"},
	"group_membership":       {"group"},
	"organization_setting":   {"organization"},
	"org_membership":         {"organization"},
	"program_membership":     {"program"},
	"subcontrol":             {"control"},
	"control_implementation": {"control"},
	"control_objective":      {"control"},
	"user_setting":           {"user"},
}

// schemaInfo holds information about an ent schema that is relevant for generating FGA permissions, such as the name of the schema and whether it has create access rules defined in its Policy function.
type schemaInfo struct {
	name                  string
	canCreateAccess       bool
	canCreateServiceOnly  bool
	excludeFromGeneration bool
	onlyAllowCreate       bool
}

const (
	// checkServiceCreateAccess is the name of the function in the policy that checks if the user has service-level create access
	checkServiceCreateAccess = "CheckServiceCreateAccess"
	// checkCreateAccess is the name of the function in the policy that checks if the user has create access (either service or user)
	checkCreateAccess = "CheckCreateAccess"
	// allowCreate is the name of the function in the policy that allows create access to everyone, this is used to determine if a schema should have a can_create relation defined for it with no access rules since it is open to everyone
	allowCreate = "AllowCreate"
	// allowForSystemAdmin is the name of the function in the policy that allows access for system admins, if a schema only has this function in its policy and no other access rules, we will exclude it from generation since it does not have any organization-level access rules defined
	allowForSystemAdmin = "AllowMutationIfSystemAdmin"
	// allowForSystemAdminQuery is the name of the function in the policy that allows query access for system admins, if a schema only has this function in its policy and no other access rules, we will exclude it from generation since it does not have any organization-level access rules defined
	allowForSystemAdminQuery = "AllowQueryIfSystemAdmin"
	// fullAccessRelation is the name of the relation for org owners and super admins
	fullAccessRelation = "full_access"
	// ownerRelation is the name of the relation for only organization owner
	ownerRelation = "owner"
)

// this generator looks at all the ent schemas and their policy functions to determine the organization-level CRUD access for each schema and generates a crud.fga file that defines the appropriate relations for each schema based on that access. This allows us to have a consistent set of permissions for each schema in FGA that are automatically generated based on the presence of certain rules in the ent policies, which reduces the likelihood of human error in defining permissions and ensures that all schemas have a consistent permission structure in FGA.
func main() {
	var (
		schemaDir = flag.String("schema-dir", "internal/ent/schema", "path to ent schema directory")
		rolesFile = flag.String("roles-file", "fga/model/roles/roles.fga", "path to FGA roles file for parsing additional CRUD access annotations")
		outFile   = flag.String("out", "fga/model/generated/crud.fga", "output FGA file")
	)
	flag.Parse()

	types, err := findAllSchemas(*schemaDir)
	if err != nil {
		log.Fatalf("finding schemas: %v", err)
	}

	roles := parseRoleAnnotations(*rolesFile)
	roles.addInheritedRoles()

	var buf bytes.Buffer

	// add headers to the generated file
	buf.WriteString("// Code generated by gen-fga-org-crud; DO NOT EDIT.\n\n")
	buf.WriteString("module base\n")
	buf.WriteString("extend type organization\n")
	buf.WriteString("  relations\n")

	for i, typeName := range types {
		fgaType := strcase.SnakeCase(typeName.name)

		canViewRelations := []string{}
		if !typeName.onlyAllowCreate {
			canViewRelations = append(canViewRelations, fmt.Sprintf("can_edit_%s", fgaType))
		}

		canEditRelations := []string{fmt.Sprintf("can_delete_%s", fgaType)}
		canDeleteRelations := []string{}
		creatorRelations := []string{fmt.Sprintf("%s_creator", fgaType)}

		canViewRelations = append(canViewRelations, roles.crudRoles[fgaType]...)
		canEditRelations = append(canEditRelations, roles.crudRoles[fgaType]...)
		canDeleteRelations = append(canDeleteRelations, roles.crudRoles[fgaType]...)
		creatorRelations = append(creatorRelations, roles.crudRoles[fgaType]...)

		canViewRelations = append(canViewRelations, roles.viewRoles[fgaType]...)

		if additionalRelations, ok := parentObjectMap[fgaType]; ok {
			for _, r := range additionalRelations {
				canViewRelations = append(canViewRelations, fmt.Sprintf("can_view_%s", r))
				canEditRelations = append(canEditRelations, fmt.Sprintf("can_edit_%s", r))
				canDeleteRelations = append(canDeleteRelations, fmt.Sprintf("can_delete_%s", r))
				creatorRelations = append(creatorRelations, fmt.Sprintf("%s_creator", r))
			}
		}

		canEditRelations = append(canEditRelations, fullAccessRelation)
		canViewRelations = append(canViewRelations, fullAccessRelation)

		// only org owners can delete the organization
		if !strings.EqualFold(fgaType, "organization") {
			canDeleteRelations = append(canDeleteRelations, fullAccessRelation)
		} else {
			canDeleteRelations = append(canDeleteRelations, ownerRelation)
		}

		// sort the relations to ensure consistent output
		slices.Sort(canViewRelations)
		slices.Sort(canEditRelations)
		slices.Sort(canDeleteRelations)
		slices.Sort(creatorRelations)

		buf.WriteString(fmt.Sprintf("    define can_view_%s: [service, user, group#member] or %s\n", fgaType, strings.Join(canViewRelations, " or ")))

		if !typeName.onlyAllowCreate {
			buf.WriteString(fmt.Sprintf("    define can_edit_%s: [service, user, group#member] or %s\n", fgaType, strings.Join(canEditRelations, " or ")))
			if len(canDeleteRelations) > 0 {

				buf.WriteString(fmt.Sprintf("    define can_delete_%s: [service, user, group#member] or %s\n", fgaType, strings.Join(canDeleteRelations, " or ")))
			} else {
				buf.WriteString(fmt.Sprintf("    define can_delete_%s: [service, user, group#member]\n", fgaType))
			}
		}

		if typeName.canCreateAccess {
			buf.WriteString(fmt.Sprintf("    define %s_creator: [group#member]\n", fgaType))
		}

		var createExpr []string
		if typeName.canCreateAccess {
			// add access for org level can_edit and can_edit_object for the object, this allows api tokens to create objects if they have "write" access to the object type.
			createExpr = append(createExpr, "can_edit")

			if !typeName.onlyAllowCreate {
				createExpr = append(createExpr, fmt.Sprintf("can_edit_%s", fgaType))
			}

			createExpr = append(createExpr, fmt.Sprintf("%s_creator", fgaType))
		}

		createExpr = append(createExpr, roles.createRoles[fgaType]...)
		createExpr = append(createExpr, roles.crudRoles[fgaType]...)

		if typeName.canCreateServiceOnly && len(createExpr) == 0 {
			createExpr = append(createExpr, "[service]")
		}

		if len(createExpr) > 0 {
			buf.WriteString(fmt.Sprintf("    define can_create_%s: %s\n", fgaType, strings.Join(createExpr, " or ")))
		}

		if i < len(types)-1 {
			buf.WriteString("\n")
		}
	}

	if err := os.MkdirAll(filepath.Dir(*outFile), 0o755); err != nil {
		log.Fatalf("creating output dir: %v", err)
	}
	if err := os.WriteFile(*outFile, buf.Bytes(), 0o644); err != nil {
		log.Fatalf("writing output file: %v", err)
	}
	log.Printf("generated %s with %d type(s)", *outFile, len(types))
}

type roleInfo struct {
	viewRoles    map[string][]string
	crudRoles    map[string][]string
	inheritRoles map[string][]string
	createRoles  map[string][]string
}

const (
	crudAnnotation    = "# @crud:"
	viewAnnotation    = "# @view:"
	inheritAnnotation = "# @inherit:"
	createAnnotation  = "# @create:"
)

// parseRoleAnnotations parses relevant annotations and role line from roles/roles.fga to determine which objects to add the roles to
func parseRoleAnnotations(rolesFile string) roleInfo {
	data, err := os.ReadFile(rolesFile)
	if err != nil {
		log.Fatalf("reading roles file: %v", err)
	}

	lines := strings.Split(string(data), "\n")

	crudMap := make(map[string][]string)
	viewMap := make(map[string][]string)
	inheritMap := make(map[string][]string)
	createMap := make(map[string][]string)

	var pendingCrud, pendingView, pendingInherit, pendingCreate []string

	isSeparator := func(c rune) bool {
		return c == ',' || c == ';' || c == ' '
	}
	for _, line := range lines {
		line = strings.TrimSpace(line)
		if strings.HasPrefix(line, crudAnnotation) {
			pendingCrud = strings.FieldsFunc(strings.TrimPrefix(line, crudAnnotation), isSeparator)
		}
		if strings.HasPrefix(line, viewAnnotation) {
			pendingView = strings.FieldsFunc(strings.TrimPrefix(line, viewAnnotation), isSeparator)
		}
		if strings.HasPrefix(line, inheritAnnotation) {
			pendingInherit = strings.FieldsFunc(strings.TrimPrefix(line, inheritAnnotation), isSeparator)
		}
		if strings.HasPrefix(line, createAnnotation) {
			pendingCreate = strings.FieldsFunc(strings.TrimPrefix(line, createAnnotation), isSeparator)
		}

		if strings.HasPrefix(line, "define ") {
			parts := strings.Fields(line)
			if len(parts) > 1 {
				role := strings.TrimSuffix(parts[1], ":")

				for _, obj := range pendingCrud {
					crudMap[obj] = append(crudMap[obj], role)
				}

				for _, obj := range pendingView {
					viewMap[obj] = append(viewMap[obj], role)
				}

				for _, in := range pendingInherit {
					inheritMap[role] = append(inheritMap[role], in)
				}

				for _, obj := range pendingCreate {
					createMap[obj] = append(createMap[obj], role)

				}
			}

			// reset pending annotations after processing a role definition
			pendingCrud = nil
			pendingView = nil
			pendingInherit = nil
			pendingCreate = nil
		}
	}

	return roleInfo{
		crudRoles:    crudMap,
		viewRoles:    viewMap,
		inheritRoles: inheritMap,
		createRoles:  createMap,
	}
}

// addInheritedRoles adds roles to objects based on the inherit annotations, so if role A inherits role B, and role B has access to edit a group, then role A should also have access to edit the group, even if it is not explicitly defined in the annotations for role A
func (r *roleInfo) addInheritedRoles() {
	for role, inherited := range r.inheritRoles {
		for _, in := range inherited {
			for obj := range r.crudRoles {
				for _, rr := range r.crudRoles[obj] {
					if rr == in {
						r.crudRoles[obj] = append(r.crudRoles[obj], role)
						break
					}
				}
			}

			for obj := range r.viewRoles {
				for _, rr := range r.viewRoles[obj] {
					if rr == in {
						r.viewRoles[obj] = append(r.viewRoles[obj], role)
						break
					}
				}
			}

			for obj := range r.createRoles {
				for _, rr := range r.createRoles[obj] {
					if rr == in {
						r.createRoles[obj] = append(r.createRoles[obj], role)
						break
					}
				}
			}
		}
	}

	// deduplicate roles for each object
	for obj := range r.crudRoles {
		r.crudRoles[obj] = lo.Uniq(r.crudRoles[obj])
	}

	for obj := range r.viewRoles {
		r.viewRoles[obj] = lo.Uniq(r.viewRoles[obj])
	}

	for obj := range r.createRoles {
		r.createRoles[obj] = lo.Uniq(r.createRoles[obj])
	}
}

// findAllSchemas looks for all checks in the ent schemas directory in order to get all schemas
// and determine the organization level CRUD access for each schema based on the presence of CheckCreateAccess and CheckServiceCreateAccess in the Policy function for each schema.
func findAllSchemas(schemaDir string) ([]schemaInfo, error) {
	entries, err := goFilesInDir(schemaDir)
	if err != nil {
		return nil, fmt.Errorf("reading schema directory: %w", err)
	}

	log.Printf("Checking all %d schemas", len(entries))

	schemas := []schemaInfo{}

	for _, file := range entries {
		fset := token.NewFileSet()
		parsedFile, err := parser.ParseFile(fset, file, nil, parser.ParseComments)
		if err != nil {
			return nil, fmt.Errorf("parsing file %s: %w", file, err)
		}

		schema := schemaInfo{}

		// not all schemas have policies, but they all should have the Name function which we can use to get the name of the schema and use that as the object type in FGA
		for _, decl := range parsedFile.Decls {
			fn, ok := decl.(*ast.FuncDecl)
			if !ok {
				continue
			}

			if fn.Name == nil || fn.Name.Name != "Name" {
				continue
			}

			recvName, ok := receiverTypeName(fn)
			if !ok {
				continue
			}

			schema.name = recvName

			break
		}

		// if the schema has a Policy function, we can check for the presence of CheckCreateAccess and CheckServiceCreateAccess to determine the create access level for the schema
		for _, decl := range parsedFile.Decls {
			fn, ok := decl.(*ast.FuncDecl)
			if !ok {
				continue
			}

			if fn.Name == nil || fn.Name.Name != "Policy" {
				continue
			}

			if fn.Body == nil {
				continue
			}

			schema.functionContainsCheckCreateAccess(fn.Body)
		}

		if schema.excludeFromGeneration {
			fmt.Println("excluding schema from generation due to only having system admin access:", schema.name)
		}

		if schema.name != "" && !schema.excludeFromGeneration {
			schemas = append(schemas, schema)
		}
	}

	slices.SortFunc(schemas, func(a, b schemaInfo) int {
		return cmp.Compare(a.name, b.name)
	})

	return schemas, nil
}

func goFilesInDir(dir string) ([]string, error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil, err
	}

	var files []string
	for _, e := range entries {
		if e.IsDir() {
			continue
		}

		if filepath.Ext(e.Name()) == ".go" && !strings.HasSuffix(e.Name(), "_test.go") {
			files = append(files, filepath.Join(dir, e.Name()))
		}
	}

	return files, nil
}

func receiverTypeName(fn *ast.FuncDecl) (string, bool) {
	if fn.Recv == nil || len(fn.Recv.List) != 1 {
		return "", false
	}

	t := fn.Recv.List[0].Type
	switch rt := t.(type) {
	case *ast.Ident:
		return rt.Name, true
	case *ast.StarExpr:
		if id, ok := rt.X.(*ast.Ident); ok {
			return id.Name, true
		}
	}

	return "", false
}

func (s *schemaInfo) functionContainsCheckCreateAccess(body *ast.BlockStmt) {
	hasSystemAdminPolicy := false
	alwaysAllowCreate := false
	countPolicy := 0
	ast.Inspect(body, func(n ast.Node) bool {
		if n == nil {
			return false
		}

		call, ok := n.(*ast.CallExpr)
		if !ok {
			return true
		}

		sel, ok := call.Fun.(*ast.SelectorExpr)
		if !ok {
			return true
		}

		pkgIdent, ok := sel.X.(*ast.Ident)
		if !ok {
			return true
		}

		if (pkgIdent.Name != "policy" && pkgIdent.Name != "rule") || sel.Sel == nil {
			return true
		}

		currentIsAdmin := false
		switch sel.Sel.Name {
		case checkServiceCreateAccess:
			s.canCreateServiceOnly = true
			s.canCreateAccess = false
		case checkCreateAccess:
			s.canCreateServiceOnly = false
			s.canCreateAccess = true
		case allowCreate:
			alwaysAllowCreate = true
			s.canCreateAccess = true
		case allowForSystemAdmin:
			hasSystemAdminPolicy = true
			currentIsAdmin = true
		}

		// if the function is not the top level "New Policy or an "With" function (e.g. WithMutationRules), update the count
		// this is to ensure we exclude anything that is just a system admin allowed mutation
		if !strings.HasPrefix(sel.Sel.Name, "NewPolicy") && !strings.HasPrefix(sel.Sel.Name, "With") && !currentIsAdmin && sel.Sel.Name != allowForSystemAdminQuery {
			countPolicy++
		}

		return true
	})

	// do not generate permissions for schemas that only have system admin access since they do not have any organization-level access rules defined
	if countPolicy == 0 && hasSystemAdminPolicy {
		s.excludeFromGeneration = true
	}

	// if there is only a create policy and no edit, and include admin only relations, this should be allowed to create but skip crud edit
	// and delete tuples
	if countPolicy == 1 && hasSystemAdminPolicy && (s.canCreateAccess || s.canCreateServiceOnly || alwaysAllowCreate) {
		s.onlyAllowCreate = true
	}
}
